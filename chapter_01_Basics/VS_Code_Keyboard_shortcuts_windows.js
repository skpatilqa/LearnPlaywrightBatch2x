// VS Code Keyboard Shortcuts for Windows
// ======================================

// General
// -------
// Command Palette                  Ctrl + Shift + P
// Quick Open (Go to File)          Ctrl + P
// New File                         Ctrl + N
// Open File                        Ctrl + O
// Save                             Ctrl + S
// Save All                         Ctrl + K, Ctrl + S
// Close Tab                        Ctrl + W
// Close All Tabs                   Ctrl + K, Ctrl + W
// Undo                             Ctrl + Z
// Redo                             Ctrl + Y  or  Ctrl + Shift + Z

// Editing
// -------
// Cut                              Ctrl + X
// Copy                             Ctrl + C
// Paste                            Ctrl + V
// Copy Line Down                   Shift + Alt + Down
// Move Line Up/Down                Alt + Up  /  Alt + Down
// Delete Line                      Ctrl + Shift + K
// Comment/Uncomment Line           Ctrl + /
// Format Document                  Shift + Alt + F
// Find                             Ctrl + F
// Replace                          Ctrl + H
// Find in Files                    Ctrl + Shift + F
// Go to Line                       Ctrl + G

// Navigation
// ----------
// Go to Definition                 F12
// Peek Definition                  Alt + F12
// Go Back                          Alt + Left
// Go Forward                       Alt + Right
// Toggle Sidebar                   Ctrl + B
// Toggle Terminal                  Ctrl + `
// Toggle Problems Panel            Ctrl + Shift + M
// Toggle Output Panel              Ctrl + Shift + U

// Terminal (Integrated)
// ---------------------
// New Terminal                     Ctrl + Shift + `
// Kill Terminal                    Ctrl + Shift + K  (when terminal focused)

// Multi-cursor & Selection
// ------------------------
// Add cursor above/below           Ctrl + Alt + Up  /  Down
// Select all occurrences           Ctrl + Shift + L
// Select current word              Ctrl + D

// Tip: Press Ctrl + K, Ctrl + S to open the Keyboard Shortcuts editor in VS Code.
